    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        try {
            String name = tfName.getText();
            String dest = txDest.getText();
            String depDateStr = txFromDate.getText();
            String reDateStr = txReturnDate.getText();
            Date depDate = new SimpleDateFormat("yyyy-MM-dd").parse(depDateStr);
            Date reDate = new SimpleDateFormat("yyyy-MM-dd").parse(reDateStr);
            
            Travel item = new Travel(name, dest, depDate, reDate);
            
            travelService.addTravel(item);
            refreshTodoList();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Exception occured: " + e.getMessage());
        }


    }//GEN-LAST:event_jButton3ActionPerformed

