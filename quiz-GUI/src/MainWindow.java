    private void refreshTodoList() {
        try {
            Travel[] itemList = travelService.getAllTravels();
            travelListModel.clear();
            for (Travel item : itemList) {
                travelListModel.addElement(item);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Exception occured: " + e.getMessage());
        }
    }

